const fahrenheit = 32; // 0c - 273.15k

// calcualte celsius and store in celsius variable
const celsius = (fahrenheit - 32) * (5 / 9);
// print that value
console.log('Celsius =', celsius + 'c');

// calcualte kelvin and store in kelvin variable
const kelvin = (fahrenheit + 459.67) * (5 / 9);
// print that value
console.log('Kelvin =', kelvin + 'k');
